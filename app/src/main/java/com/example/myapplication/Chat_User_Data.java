package com.example.myapplication;

public class Chat_User_Data {
    private String Chat_User_Data;
    private String Chat_User_Img;

    public Chat_User_Data(String chat_User_Data, String chat_User_Img) {
        Chat_User_Data = chat_User_Data;
        Chat_User_Img = chat_User_Img;
    }

    public String getChat_User_Data() {
        return Chat_User_Data;
    }

    public void setChat_User_Data(String chat_User_Data) {
        Chat_User_Data = chat_User_Data;
    }

    public String getChat_User_Img() {
        return Chat_User_Img;
    }

    public void setChat_User_Img(String chat_User_Img) {
        Chat_User_Img = chat_User_Img;
    }
}
